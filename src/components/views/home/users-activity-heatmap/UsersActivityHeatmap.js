const UsersActivityHeatmap = ({data, loading, error}) => {

   let heatmapPart = getResultByPartId(HOME_USERS_ACTIVITY_HEATMAP_PART, data);
   const graphData = heatmapPart ? 
      heatmapPart.data.map((row) => [row.dimensionValues[1], row.dimensionValues[0], row.metricValues[0]]) : [];

   const containerId = "activityHeatmapContainer";
   return (
      <ViewBox type={VERTICAL_VIEW_BOX} headerTitle="Users Activity" footerVisible={false} loading={loading}>
         <ActivityHeatmap data={graphData} xArray={xArray} yArray={yArray} containerId={containerId} />
      </ViewBox>
   );
};

